                DefaultTableModel model = (DefaultTableModel) jTableFilme.getModel();
                model.addRow(new Object[]{
                    filme.getCodFilme(),
                    filme.getTitulo(),
                    filme.getAtor(),
                    filme.getGenero(),
                    filme.getMidia()
                });

