    private void jButtonEncontrarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEncontrarClienteActionPerformed

        int codigo = 0;
        String status;

        try {
            codigo = Integer.parseInt(jTextFieldCodCliente.getText());
            Cliente cliente;

            cliente = this.clienteController.buscaCliente(codigo);
            if (cliente != null) {
                jTextFieldNomeCliente.setText(cliente.getNome());
                jFormattedCpf.setText(cliente.getCpf());
                // seta o codigo do cliente na parte da Caixa
                jTextFieldCodigoClienteCaixa.setText(String.valueOf(codigo));

                try {
                    status = cliente.getStatus();
                    if (!status.equals("pendente")) {
                        jTextFieldStatus.setText(status);
                    }
                } catch (NullPointerException e) {
                    jTextFieldStatus.setText("pendente");
                }

                Object cli[] = {
                    codigo,
                    cliente.getNome(),
                    cliente.getCpf(),
                    cliente.getDtNascimento(),
                    cliente.getEndereco(),
                    cliente.getEmail()
                };

                DefaultTableModel model = (DefaultTableModel) jTableCliente.getModel();

                model.addRow(cli);

            } else {
                JOptionPane.showMessageDialog(null, "Não foi encontrado cliente com o"
                        + " código informado.");
                jTextFieldCodCliente.setText("");
                jTextFieldNomeCliente.setText("");
                jFormattedCpf.setText("");
                jTextFieldStatus.setText("");
            }

        } catch (NumberFormatException numberFormatException) {
            jTextFieldCodCliente.setText("");
        }

        if (codigo > 0) {
        } else {
            JOptionPane.showMessageDialog(null, "digite o código do cliente");
        }
    }//GEN-LAST:event_jButtonEncontrarClienteActionPerformed

