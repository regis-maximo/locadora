    public void horaStart() {

        Date data = new Date();
        SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");

        // data do jFormattedDataLocacao do Caixa
        jFormattedDataLocacao.setText(formato.format(data));

        th = new Thread(() -> {
            while (this.executaHora == true) { //roda indefinidamente
                Date date = Calendar.getInstance().getTime(); //pega a hora do sistema
                DateFormat formatter = new SimpleDateFormat("HH:mm:ss");
                String hora = formato.format(data) + " - " + formatter.format(date);
                jLabelHora.setText(hora);
                try {
                    Thread.sleep(1000); //espera 1 segundo para fazer a nova evolução
                } catch (InterruptedException ex) {
                    //é algo terrível a se fazer mas pelo jeito a API medonha do Java exige
                }
            }
        } //cria uma thread
        );
        th.start();
    }

    // método utilitário para formatar data
    private java.sql.Date formatadorData(String dataSTR) {
        java.sql.Date dataSQL = null;
        try {
            SimpleDateFormat formata = new SimpleDateFormat("dd/MM/yyyy");
            String dataRecebida = dataSTR;
            Date data = formata.parse(dataRecebida);
            dataSQL = new java.sql.Date(data.getTime());

        } catch (ParseException ex) {
            System.err.println("err: " + ex.getMessage());
        }
        return dataSQL;
    }
