    private void tabelaAtor() {

        DefaultTableModel model = (DefaultTableModel) jTablePreAtores.getModel();

        DefaultTableModel dm = new DefaultTableModel() {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };

        dm.addColumn(model.getColumnName(0));
        dm.addColumn(model.getColumnName(1));
        dm.addColumn(model.getColumnName(2));

        for (Ator ator : this.atorController.atualizarTabela()) {
            dm.addRow(new Object[]{
                ator.getCodAtor(),
                ator.getNome(),
                ator.getNacionalidade()
            });
        }

        jTablePreAtores.setModel(dm);

    }

    private void tabelaFilme() {
        // pega o modelo da tabela do JFrame 
        DefaultTableModel model = (DefaultTableModel) jTablePreFilmes.getModel();

        // cria um objeto DefaultTableModel para poder sobreescrever o método
        // isCellEditable da classe DefaultTableModel para poder bloquear a 
        // digitacão do usuario
        DefaultTableModel dm = new DefaultTableModel() {

            @Override
            public boolean isCellEditable(int row, int col) {
                return false;
            }
        };

        // pega o nome das colunas do objeto que pegamos da JtablePreFilmes
        dm.addColumn(model.getColumnName(0));
        dm.addColumn(model.getColumnName(1));
        dm.addColumn(model.getColumnName(2));
        dm.addColumn(model.getColumnName(3));
        dm.addColumn(model.getColumnName(4));
        dm.addColumn(model.getColumnName(5));

        //  cria um novo objeto do tipo Filme a cada iteração percorrida nos
        // dados recebidos do banco de dados através do método atualizarTabela
        // e coloca cada objeto do tipo Filme em cada linha da tabela (dm).
        for (Filme filme : this.filmeController.atualizarTabela()) {
            dm.addRow(new Object[]{
                filme.getCodFilme(),
                filme.getTitulo(),
                filme.getAtor(),
                filme.getGenero(),
                filme.getDuracao(),
                filme.getMidia(),
                filme.getPreco()

            });

            // Adiciona a tabela (dm) que retorna false para todas as linhas 
            // para poder ser feito bloqueio de digitação.
            jTablePreFilmes.setModel(dm); // adiciona modelo dm a tabela jTablePreFilmes
           
        }
    }

