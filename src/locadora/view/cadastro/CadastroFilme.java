    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        int duracao = Integer.parseInt(jSpinnerDuracao.getValue().toString());
        String genero = (String) jComboBoxGenero.getSelectedItem();
        double preco = 0;

        try {
            preco = Double.parseDouble(jFormattedTextFieldPreco.getText().replace(",", "."));

        } catch (NumberFormatException numberFormatException) {
            return;
        }

        if (jTextFieldTitulo.getText().equals("") || jTextAreaSinopse.getText().equals("")
                || genero.equals("Selecione um gênero")
                || jTextFieldAtor.getText().equals("")
                || jFormattedTextFieldPreco.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Todos os campos são obrigatórios!");
            return;
        }

        try {
            FilmeController filmeController = new FilmeController();
            filmeController.cadastrarFilme(jTextFieldTitulo.getText(),
                    genero, jTextAreaSinopse.getText(), duracao, jTextFieldAtor.getText(),
                    preco, (String) jComboBoxMidia.getSelectedItem());

        } catch (HeadlessException e) {
            System.err.println("Erro no jButtonSalver: " + e.getMessage());
        }
        // limpar todos os campos quando o usuário clicar no botão salvar
        jTextFieldTitulo.setText("");
        jComboBoxGenero.setSelectedIndex(0);
        jTextAreaSinopse.setText("");
        jTextFieldAtor.setText("");
        jFormattedTextFieldPreco.setText("");
        jSpinnerDuracao.setValue(20);
        
        // atualiza a tabela com o novo filme cadastrado
        DefaultTableModel limpar = (DefaultTableModel) jTablePreFilmes.getModel();
        limpar.setNumRows(0);
        tabelaFilme();
    }//GEN-LAST:event_jButtonSalvarActionPerformed

