    private void mostrarTabela() {

        DefaultTableModel tableModel = (DefaultTableModel) jTableFilmes.getModel();
        FilmeController filmes = new FilmeController();
        
        for (Filme filme : filmes.atualizarTabela()) {
            tableModel.addRow(new Object[]{
                filme.getCodFilme(),
                filme.getTitulo(),
                filme.getAtor(),
                filme.getGenero(),
                filme.getSinopse(),
                filme.getDuracao(),
                filme.getMidia(),
                filme.getPreco()
                    
            });

        }
    }

