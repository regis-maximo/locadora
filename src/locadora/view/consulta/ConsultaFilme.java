    private void jButtonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmarActionPerformed
        
        int codigo = 0;
        String titulo = null;
        String ator = null;
        String genero = null;
        String midia = null;
        double preco = 0;
        int duracao = 0;
        try {
            codigo = Integer.parseInt(jTextFieldCodFilme.getText());
            duracao = Integer.parseInt(jTextFieldDuracao.getText());
            preco = Double.parseDouble(jFormattedTextFieldPreco.getText().replace(",", "."));
            titulo = jTextFieldTituloFilme.getText();
            midia = jTextFieldMidia.getText();
            ator = jTextFieldAtor.getText();
            genero = jTextFieldGeneroFilme.getText();
            
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(null, "Digitar somente número em preço!");
            jFormattedTextFieldPreco.requestFocus();
            return;
        }
                
        if (codigo > 0) {
            
            Filme filme = new Filme(codigo, titulo, ator, genero, genero, midia, preco, duracao);
            FilmeController filmeController = new FilmeController();
            filmeController.editarFilme(filme);
 
            // Limpa todos os campos e desahilita os botões Editar e Confirmar
            jTextFieldPesquisaFilme.setText("");
            jTextFieldCodFilme.setText("");
            jTextFieldTituloFilme.setText("");
            jTextFieldAtor.setText("");
            jTextFieldGeneroFilme.setText("");
            jTextFieldMidia.setText("");
            jFormattedTextFieldPreco.setText("");
            jTextAreaSinopseFilme.setText("");
            jTextFieldDuracao.setText("");

            // Desabilita os botões Editar e Confirmar
            jButtonEditar.setEnabled(false);
            jButtonConfirmar.setEnabled(false);
            
            // Atualiza a Tabela
            DefaultTableModel atualizar = (DefaultTableModel) jTableFilmes.getModel();
            atualizar.setNumRows(0);
            mostrarTabela();
       }
    }//GEN-LAST:event_jButtonConfirmarActionPerformed

