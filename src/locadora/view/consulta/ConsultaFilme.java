    private void jButtonBuscarFilmeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarFilmeActionPerformed
        FilmeController filmeController = new FilmeController();
//        filmeController.consultarFilme(jTextFieldPesquisaFilme.getText());

        Filme filmeE = filmeController.consultarFilme(jTextFieldPesquisaFilme.getText());
        if (filmeE != null) {
            try {
                jTextFieldCodFilme.setText(String.valueOf(filmeE.getCodFilme()));
                jTextFieldMidia.setText(filmeE.getTitulo());
                jTextFieldTituloFilme.setText(filmeE.getTitulo());
                jFormattedTextFieldPreco.setText(String.valueOf(filmeE.getPreco()));
                jTextFieldAtor.setText(filmeE.getAtor());
                jTextFieldGeneroFilme.setText(filmeE.getGenero());
                jTextAreaSinopseFilme.setText(filmeE.getSinopse());
                jTextFieldDuracao.setText(String.valueOf(filmeE.getDuracao()));

                // Habilita o botão Editar
                jButtonEditar.setEnabled(true);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "digite número no campo preço!"+e);
                return;
            }

        } else {
            JOptionPane.showMessageDialog(null, "Filme não encontrado.");
            jTextFieldPesquisaFilme.setText("");
            jTextFieldCodFilme.setText("");
            jTextFieldMidia.setText("");
            jTextFieldGeneroFilme.setText("");
            jTextAreaSinopseFilme.setText("");
            jTextFieldDuracao.setText("");
        }
    }//GEN-LAST:event_jButtonBuscarFilmeActionPerformed

