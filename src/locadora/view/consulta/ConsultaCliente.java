    private void jButtonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmarActionPerformed

        try {
            Cliente cliente = new Cliente();
            cliente.setCodCliente(Integer.parseInt(jTextFieldCodCliente.getText()));
            cliente.setNome(jTextFieldNome.getText());
            cliente.setCpf(jFormattedTextFieldCpf.getText());

            String data = jFormattedTextFieldDtNasc.getText();
            SimpleDateFormat formatar = new SimpleDateFormat("dd/MM/yyyy");
            java.sql.Date dataSql = new java.sql.Date(formatar.parse(data).getTime());

            cliente.setDtNascimento(dataSql);

            cliente.setEndereco(jTextFieldEndereco.getText());
            cliente.setEmail(jTextFieldEmail.getText());
            this.clienteController.updateCliente(cliente);

            // limpa tabela e atuliza com novo dado editado atualizado
            DefaultTableModel limpar = (DefaultTableModel) jTableCliente.getModel();
            limpar.setNumRows(0);
            mostrarTabela();

            // limpa todos campos e desabilita os botôes editar e confirmar
            jTextFieldPesquisa.setText("");
            jTextFieldCodCliente.setText("");
            jTextFieldNome.setText("");
            jFormattedTextFieldCpf.setText("");
            jFormattedTextFieldDtNasc.setText("");
            jTextFieldEndereco.setText("");
            jTextFieldEmail.setText("");

            // desativa os botôes editar e confirmar
            jButtonEditar.setEnabled(false);
            jButtonConfirmar.setEnabled(false);

        } catch (ParseException ex) {
            System.out.println(">>> " + ex.getMessage());
        }
    }//GEN-LAST:event_jButtonConfirmarActionPerformed

    private void jButtonPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarActionPerformed

        if (!"".equals(jTextFieldPesquisa.getText())) {
            Cliente cliente = this.clienteController.buscaCliente(jTextFieldPesquisa.getText());
            jTextFieldNome.setText(cliente.getNome());
            jTextFieldCodCliente.setText(String.valueOf(cliente.getCodCliente()));
            jFormattedTextFieldCpf.setText(cliente.getCpf());
            jFormattedTextFieldDtNasc.setText(cliente.getDtNascimento());
            jTextFieldEndereco.setText(cliente.getEndereco());
            jTextFieldEmail.setText(cliente.getEmail());

            // habilita botão editar
            jButtonEditar.setEnabled(true);
        }
    }//GEN-LAST:event_jButtonPesquisarActionPerformed

