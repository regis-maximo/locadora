    private void mostrarTabela() {

        DefaultTableModel model = (DefaultTableModel) jTableCliente.getModel();
        for (Cliente cliente : this.clienteController.atualizaTabela()) {
            model.addRow(new Object[]{
                cliente.getCodCliente(),
                cliente.getNome(),
                cliente.getCpf(),
                cliente.getDtNascimento(),
                cliente.getEndereco(),
                cliente.getEmail()
            });
        }
    }
