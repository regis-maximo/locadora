    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed

        if (!jTextFieldNomeAtor.getText().equals("") && !jTextFieldNacionalidadeAtor.getText().equals("")) {
            jTextFieldPesquisaAtor.setText("");
            jTextFieldNomeAtor.setEditable(true);
            jTextFieldNacionalidadeAtor.setEditable(true);

            jTextFieldNomeAtor.requestFocus();
            jButtonConfirmar.setEnabled(true);
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

    private void jButtonConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConfirmarActionPerformed

        Ator ator = new Ator();
        ator.setCodAtor(Integer.parseInt(jTextFieldCodigoAtor.getText()));
        ator.setNome(jTextFieldNomeAtor.getText());
        ator.setNacionalidade(jTextFieldNacionalidadeAtor.getText());

        atorController.editarAtor(ator);
        jButtonEditar.setEnabled(false);
        jButtonConfirmar.setEnabled(false);

        DefaultTableModel limpar = (DefaultTableModel) jTableAtores.getModel();
        limpar.setNumRows(0);

        // chama método para atualizar a tabela depois que editar e confirma
        mostrarTabela();

        // limpa todos os campos e desabilita os botões editar e confirmar
        jTextFieldPesquisaAtor.setText("");
        jTextFieldNomeAtor.setText("");
        jTextFieldNacionalidadeAtor.setText("");
        jTextFieldCodigoAtor.setText("");

        jButtonEditar.setEnabled(false);
        jButtonConfirmar.setEnabled(false);
    }//GEN-LAST:event_jButtonConfirmarActionPerformed

