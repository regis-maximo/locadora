    private void mostrarTabela() {
        DefaultTableModel model = (DefaultTableModel) jTableAtores.getModel();

        for (Ator ator : atorController.atualizarTabela()) {
            model.addRow(new Object[]{
                ator.getCodAtor(),
                ator.getNome(),
                ator.getNacionalidade()});
        }
    }

