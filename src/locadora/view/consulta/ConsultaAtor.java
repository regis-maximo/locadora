    private void jButtonPesquisarAtorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonPesquisarAtorActionPerformed
        //pesquisar
        try {
            if (jTextFieldPesquisaAtor.getText() != null) {
                Ator ator = atorController.pesquisar(jTextFieldPesquisaAtor.getText());

                jTextFieldNomeAtor.setText(ator.getNome());
                jTextFieldNacionalidadeAtor.setText(ator.getNacionalidade());
                jTextFieldCodigoAtor.setText(String.valueOf(ator.getCodAtor()));
                jButtonEditar.setEnabled(true); // habilita o botão editar
            }
        } catch (NullPointerException e) {

            jTextFieldPesquisaAtor.setText("");
            return;
        }


    }//GEN-LAST:event_jButtonPesquisarAtorActionPerformed

